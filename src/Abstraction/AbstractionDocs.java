package Abstraction;

public class AbstractionDocs {
	
	/*
	 * --------------Abstraction--------------
	 * =>process of hiding implementation details in the program 
	 * 
	 * =>to achieve abstraction
	 * 	1.Abstract class
	 * 		=>class which contains abstract methods(unimplemented methods)
	 * 		 and non-abstract methods(implemented method)
	 * 		#Abstract method-method which does not have body/implementation
	 * 		e.g:
	 * 			abstract void sum();
	 * 			abstract int getValue(String msg);
	 * 
	 * 		#every abstract class must have at least one child class and child class
	 * 		must have override abstract class
	 * 
	 * 		#we can't create object of abstract class.
	 * 		#level of abstraction=0 to 100%
	 * 
	 * 		syntax:
	 * 			abstract class class_name{
	 * 				//abstract method
	 * 				
	 * 				//non-abstract method
	 * 			}
	 * 
	 * 	2.Interface
	 * 		=>same as class which contains public abstract methods and public static
	 * 			final variables.
	 * 			
	 * 			#we can't create object of interface
	 * 
	 * 			#every interface must have at least one implementation class and
	 * 			implementation class must override abstract class
	 * 
	 * 			#level of abstraction 100%
	 * 
	 * 			syntax:
	 * 				interface interface_name{
	 * 
	 * 					//public abstract methods
	 * 				}
	 */		

}
